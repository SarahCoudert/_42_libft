#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char		*dp;
	const char	*sp;
	
	sp = s2;
	dp = s1;
	while (n--)
		*dp++ = *sp++;
	return (s1);
}
