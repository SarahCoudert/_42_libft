#include "libft.h"

size_t	strlcat(char *dst, const char *src, size_t size)
{
	size_t dst_len;
	size_t i;

	dst_len = strlen(dst);
	i = 0;
	while (i < size && src[i] != '\0')
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	return dst_len;
}
