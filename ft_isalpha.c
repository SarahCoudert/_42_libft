#include "libft.h"

int		ft_isalpha(int c)
{

#if defined(VERBOSE)
	ft_putstr("ft_isalpha\n");
#endif
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
