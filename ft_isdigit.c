#include "libft.h"

int		ft_isdigit(int c)
{
#if defined(VERBOSE)
	ft_putstr("ft_isdigit");
#endif
	if (c >= '0' && c <= '9')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
